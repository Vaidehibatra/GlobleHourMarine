   <script>
   // Function to trigger the experience counter
   function triggerExperienceCounter() {
      const counters = document.querySelectorAll(".experience");

      counters.forEach((counter) => {
         counter.innerText = "0";
         const updateCounter = () => {
            const target = +counter.getAttribute("data-target");
            const count = +counter.innerText;
            const increment = target / 10;
            if (count < target) {
               counter.innerText = `${Math.ceil(count + increment)}`;
               setTimeout(updateCounter, 100); // Slowed down to 100ms
            } else counter.innerText = target;
         };
         updateCounter();
      });
   }

   // Function to trigger the clients counter
   function triggerClientsCounter() {
      const counterss = document.querySelectorAll(".clients");

      counterss.forEach((counter) => {
         counter.innerText = "0";
         const updateCounter = () => {
            const target = +counter.getAttribute("data-target");
            const count = +counter.innerText;
            const increment = target / 200;
            if (count < target) {
               counter.innerText = `${Math.ceil(count + increment)}`;
               setTimeout(updateCounter, 1); // Faster at 1ms
            } else counter.innerText = target;
         };
         updateCounter();
      });
   }

   // Function to trigger the satisfaction counter
   function triggerSatisfactionCounter() {
      const countersss = document.querySelectorAll(".satisfaction");

      countersss.forEach((counter) => {
         counter.innerText = "0";
         const updateCounter = () => {
            const target = +counter.getAttribute("data-target");
            const count = +counter.innerText;
            const increment = target / 200;
            if (count < target) {
               counter.innerText = `${Math.ceil(count + increment)}`;
               setTimeout(updateCounter, 10); // Moderately fast at 10ms
            } else counter.innerText = target;
         };
         updateCounter();
      });
   }

   // IntersectionObserver to check when the section is in view
   const section = document.querySelector('#t-counter');
   const observer = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
         if (entry.isIntersecting) {
            // Section is in view, trigger the counters
            triggerExperienceCounter();
            triggerClientsCounter();
            triggerSatisfactionCounter();
            observer.unobserve(entry.target); // Stop observing once triggered
         }
      });
   }, { threshold: 0.5 }); // Trigger when 50% of the section is in view

   observer.observe(section);
</script>
