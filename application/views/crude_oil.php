               <h3 class="toc-container-faq" id="What-Is-Crude-Oil?">What Is Crude Oil?</h3>
               <p>Crude oil is a natural, unrefined petroleum product composed of hydrocarbon deposits and other organic materials. It is a key source of energy and raw materials used in the production of many everyday products.</p>

               <h3 class="toc-container-faq" id="Why-Is-It-Called-Crude-Oil?">Why Is It Called Crude Oil?</h3>
               <p>The term “crude oil” refers to the raw state in which oil is extracted from underground sources. This raw oil is a complicated mixture of hydrocarbons with trace levels of sulfur, nitrogen, oxygen, and other impurities. The word “crude” highlights the fact that the oil has not yet undergone any refining process to separate these different components into more useful products such as gasoline, diesel, jet fuel, or petrochemicals.</p>

               <h3 class="toc-container-faq" id="Is-Crude-Oil-Gasoline?">Is Crude Oil Gasoline?</h3>
               <p>No, crude oil is not gasoline; however, gasoline is a fuel made from crude oil and other petroleum liquids. Crude oil is a raw material that is refined to produce various products, including gasoline.</p>

               <h3 class="toc-container-faq" id="How-Crude-Oil-Is-Made?">How Crude Oil Is Made?</h3>
               <p>Crude oil is a blend of hydrocarbons, which are molecules made up of carbon and hydrogen. It originates from the decay of plant and animal matter that settled at the bottom of ancient oceans and lakes. Over millions of years, this organic material became buried under layers of sediment, experiencing intense pressure and heat, eventually transforming into crude oil.</p>
               <ol class="sub-heading-crude-oil">
                  <li>Extracting Crude Oil</li>
                  <p>Crude oil is formed over millions of years from the remains of tiny sea plants and animals that lived in ancient oceans. These organic materials accumulated on the ocean floor and were gradually buried under layers of sediment.</p>
                  <li>Formation of Oil Reservoirs</li>
                  <p>Over time, the layers of sediment above the organic materials exerted pressure and heat, causing them to undergo chemical changes. This process, known as diagenesis, transforms the organic matter into hydrocarbons, which are the primary components of crude oil.</p>
                  <li>Migration and Trapping</li>
                  <p>As the hydrocarbons formed, they migrated through porous rock layers, such as sandstone or limestone, in search of impermeable traps where they could accumulate. These traps could be formed by geological structures like anticlines, faults, or salt domes, which prevent the oil from escaping to the surface.</p>
                  <li>Drilling and Extraction</li>
                  <p>To access crude oil trapped underground, drilling rigs are used to bore deep into the Earth’s crust. Once a suitable reservoir is located, a well is drilled, and specialized equipment is employed to extract the crude oil. This process involves pumping the oil to the surface using a combination of mechanical pumps and natural pressure from the reservoir.</p>
                  <li>Transportation and Refining</li>
                  <p>After extraction, the crude oil is transported via pipelines, ships, or trucks to refineries. At the refinery, the crude oil undergoes a series of refining processes to separate it into various components, such as gasoline, diesel, and jet fuel, based on their molecular weights and boiling points.</p>
                  <li>Utilization</li>
                  <p>The refined products derived from crude oil are used in a wide range of applications, including transportation, heating, electricity generation, and manufacturing. These products play essential roles in powering modern societies and industries, making crude oil a vital resource in the global economy.</p>
               </ol>

               <h3 class="toc-container-faq" id="What-Are-The-Six-Types-of-Crude-Oil?">What Are The Six Types of Crude Oil?</h3>
               <p>There are six types of crude oil based on its consistency:</p>
               <ol class="sub-heading-crude-oil-points">
                  <li>Light/Sweet Crude Oil</li>
                  <p>Light/Sweet crude oil is characterized by its low density and sulfur content, making it easier to refine into products like gasoline and diesel. This type of crude oil is commonly found in areas with sedimentary rock formations, such as shale or sandstone, and is often extracted from regions like the North Sea, West Texas, and Nigeria. Light/sweet crude oil is highly sought after due to its high yield of valuable refined products.</p>
                  <li>Heavy/Sour Crude Oil</li>
                  <p>Heavy/sour crude oil has a higher density and higher sulfur content compared to light crude oil. It requires more extensive refining to produce usable fuels and other products. Heavy/sour crude oil is typically found in regions like Canada and Venezuela, where it is extracted from tar sands and heavy oil deposits.</p>
                  <li>Medium Crude Oil</li>
                  <p>Medium crude oil is a blend of light and heavy crude oils, falling somewhere in between in terms of density and sulfur content. It can be refined into a variety of products and is often sourced from countries like Mexico and the United States.</p>
                  <li>Ultra-Light Crude Oil</li>
                  <p>Ultra-light crude oil is extremely low in density and has very low sulfur content. It can yield a high percentage of refined products during the refining process. This type of crude oil is rare and typically found in limited quantities.</p>
                  <li>Extra-Heavy Crude Oil</li>
                  <p>Extra-heavy crude oil is extremely dense and viscous, making it challenging to extract and refine. It often requires advanced techniques like thermal recovery or solvent extraction. This type of crude oil is found in places like Canada’s oil sands and is usually processed into synthetic crude oil.</p>
                  <li>Oil Sands</li>
                  <p>Oil sands are a mixture of sand, water, clay, and bitumen, a thick, tar-like substance that can be processed to extract crude oil. Oil sands are primarily found in Canada and Venezuela and require specialized extraction methods due to their solid nature.</p>
               </ol>
